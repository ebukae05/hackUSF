def _dicts_to_resources(raw: list[dict]) -> list[Resource]:
    from services.relieflink_agents.models import Location, ResourceType, ResourceStatus
    result = []
    for r in raw:
        try:
            loc_raw = r.get("location") or {}
            location = Location(
                lat=float(loc_raw.get("lat", 0.0)),
                lon=float(loc_raw.get("lon", 0.0)),
                address=loc_raw.get("address", ""),
                fips_code=loc_raw.get("fips_code", ""),
            ) if loc_raw else None
            result.append(Resource(
                resource_id=r.get("resource_id", ""),
                type=ResourceType(r.get("type", "supplies")),
                subtype=r.get("subtype", ""),
                quantity=int(r.get("quantity", 0)),
                location=location,
                owner_agency_id=r.get("owner_agency_id", ""),
                status=ResourceStatus(r.get("status", "available")),
            ))
        except (ValueError, KeyError):
            continue
    return result


def _dicts_to_needs(raw: list[dict]) -> list[Need]:
    from services.relieflink_agents.models import NeedType
    result = []
    for n in raw:
        try:
            result.append(Need(
                need_id=n.get("need_id", ""),
                community_fips_tract=n.get("community_fips_tract", ""),
                need_type=NeedType(n.get("need_type", "supplies")),
                severity=float(n.get("severity", 0.0)),
                quantity_needed=int(n.get("quantity_needed", 0)),
                quantity_fulfilled=int(n.get("quantity_fulfilled", 0)),
                equity_score=float(n.get("equity_score", 0.0)),
            ))
        except (ValueError, KeyError):
            continue
    return result


def _dicts_to_communities(raw: list[dict]) -> list[Community]:
    from services.relieflink_agents.models import SVIThemes
    result = []
    for c in raw:
        try:
            themes_raw = c.get("svi_themes") or {}
            svi_themes = SVIThemes(
                socioeconomic=float(themes_raw.get("socioeconomic", 0.5)),
                household=float(themes_raw.get("household", 0.5)),
                minority=float(themes_raw.get("minority", 0.5)),
                housing_transport=float(themes_raw.get("housing_transport", 0.5)),
            ) if themes_raw else None
            result.append(Community(
                fips_tract=c.get("fips_tract", ""),
                county_fips=c.get("county_fips", ""),
                state=c.get("state", "FL"),
                population=int(c.get("population", 0)),
                vulnerability_index=float(c.get("vulnerability_index", 0.5)),
                svi_themes=svi_themes,
                county_name=c.get("county_name", ""),
            ))
        except (ValueError, KeyError):
            continue
    return result


def _match_to_dict(m: Match) -> dict:
    routing = None
    if m.routing_plan:
        routing = {
            "origin": m.routing_plan.origin,
            "destination": m.routing_plan.destination,
            "distance_km": m.routing_plan.distance_km,
            "eta_hours": m.routing_plan.eta_hours,
        }
    return {
        "match_id": m.match_id,
        "resource_id": m.resource_id,
        "need_id": m.need_id,
        "equity_score": m.equity_score,
        "routing_plan": routing,
        "status": m.status.value,
        "convergence_note": m.convergence_note,
    }


def _resource_to_dict(r: Resource) -> dict:
    return {
        "resource_id": r.resource_id,
        "type": r.type.value,
        "subtype": r.subtype,
        "quantity": r.quantity,
        "location": {
            "lat": r.location.lat, "lon": r.location.lon,
            "address": r.location.address, "fips_code": r.location.fips_code,
        } if r.location else None,
        "owner_agency_id": r.owner_agency_id,
        "status": r.status.value,
    }


def _need_to_dict(n: Need) -> dict:
    return {
        "need_id": n.need_id,
        "community_fips_tract": n.community_fips_tract,
        "need_type": n.need_type.value,
        "severity": n.severity,
        "quantity_needed": n.quantity_needed,
        "quantity_fulfilled": n.quantity_fulfilled,
        "equity_score": n.equity_score,
    }


def _community_to_dict(c: Community) -> dict:
    return {
        "fips_tract": c.fips_tract,
        "county_fips": c.county_fips,
        "state": c.state,
        "population": c.population,
        "vulnerability_index": c.vulnerability_index,
        "county_name": c.county_name,
        "svi_themes": {
            "socioeconomic": c.svi_themes.socioeconomic,
            "household": c.svi_themes.household,
            "minority": c.svi_themes.minority,
            "housing_transport": c.svi_themes.housing_transport,
        } if c.svi_themes else None,
    }
